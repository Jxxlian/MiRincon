            <p>Flora Alejandra Pizarnik (Avellaneda, 29 de abril de 1936 - Buenos Aires, 25 de septiembre de 1972) fue una poetisa, ensayista y traductora argentina<br><br>

            Estudió filosofía y Letras en la Universidad de Buenos Aires y más tarde, pintura con Juan Batlle Planas. Entre 1960 y 1964 Pizarnik vivió en París, donde trabajó para la revista Cuadernos y algunas editoriales francesas, publicó poemas y críticas en varios diarios y tradujo a Antonin Artaud, Henri Michaux, Aimé Césaire e Yves Bonnefoy. Además, estudió historia de la religión y literatura francesa en La Sorbona. <br><br>

            Tras su retorno a Buenos Aires, Pizarnik publicó tres de sus principales volúmenes: Los trabajos y las noches, Extracción de la piedra de locura y El infierno musical, así como su trabajo en prosa La condesa sangrienta. En 1969 recibió una beca Guggenheim, y en 1971 una Fullbright. El 25 de septiembre de 1972, mientras pasaba un fin de semana fuera de la clínica psiquiátrica donde estaba internada, Pizarnik murió de una sobredosis, tras ingerir cincuenta pastillas de un psicotrópico conocido comercialmente como Seconal. <br><br>

           Sus trabajos y su poesía dejaron un legado de un valor incalculable para la literatura latinoamericana. A partir del retorno de la democracia en Argentina, la figura de Pizarnik, al igual que la muchas otras escritoras del boom latinoamericano, experimentó un auge, lo que derivó en la primera compilación de sus textos Textos de Sombra y últimos poemas (1982), seguido de su primera biografía, Alejandra (1991) de la parte de Cristina Piña. Más reciententemente, se han publicado también sus Diarios (2013). </p>
